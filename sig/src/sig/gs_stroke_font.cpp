void GsFontComputeDimensions ()
 {
   int c, i, n, s, x, y;
   gschar* data;
   for ( c=0; c<95; c++ )
	{ data = Simplex[c]; // first entry
	  n = (int) *data++;
	  s = (int) *data++;
	  if ( s>MaxS ) MaxS=s;
	  for ( i=0; i<n; i++ )
	   { x = *data++;
		 y = *data++;
		 if ( x==-1 && y==-1 ) continue;
		 if ( x>MaxW ) MaxW=x;
		 if ( y>MaxY ) MaxY=y;
		 if ( y<MinY ) MinY=y;
	   }
	}
   MaxH = MaxY - MinY;
   gsout<< MaxS <<","<< MaxW << " : "<< MinY <<","<< MaxY<<","<< MaxH <<gsnl;
 }

