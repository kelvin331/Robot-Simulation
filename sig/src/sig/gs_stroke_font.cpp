int GsStrokeFont::MaxS=30, // maximum horizontal spacing
	GsStrokeFont::MaxW=26, // maximum character width
	GsStrokeFont::MinY=-7, // minimum character Y coordinate with respect to base
	GsStrokeFont::MaxY=25, // maximum character Y coordinate with respect to base
	GsStrokeFont::MaxH=32; // maximum character height

void GsStrokeFont::draw ( const char* text, const GsPnt2& p, Output& out, float charh, float wperc, float linespc, float charspc, bool wcoords, float* xmax )
 {
   if ( !text ) return;
   GS_TRACE1 ( "GsFont::draw at "<<p<<" ["<<text<<']' );

   int c;	// current char index
   int i;	// current vertex index
   int n;	// current char number of vertices
   const gschar* data;
   float x, y, ocx=p.x, curx=p.x, cury=p.y;
   const GsStrokeFont::ChDesc* chdesc;

   float scalingy = scaling ( wcoords? -charh:charh );
   float scalingx = scalingy*wperc/100.0f;

   for ( c=0; text[c]; c++ )
	{
	  if ( text[c]=='\n' )
	   { curx=p.x; cury+=float(GsStrokeFont::MaxH)*-scalingy + float(linespc);
		 continue;
	   }

	  chdesc = &GsStrokeFont::SimplexChar[text[c]-32]; // first entry
	  n = (int) chdesc->n; // number of vertices for current character
	  data = GsStrokeFont::SimplexData + chdesc->i; // first entry

	  if ( n>0 ) out.begin(); // begin
	  for ( i=0; i<n; i++ )
	   { x = float(*data++);
		 y = float(*data++);
		 if ( x==-1 && y==-1 ) // pen up
		  { out.end(); // end
			out.begin(); // begin
		  }
		 else
		  { x = x * scalingx;
			y = y * scalingy;
			float cx = curx+x;
			if ( xmax && cx>*xmax ) // max len check
			 { *xmax=ocx; 
			   while ( text[++c]!='\n' ) { if(!text[c]) { out.end(); return; } }
			   c--;
			 } 
			else
			 { out.add ( cx, cury+y );
			   ocx = cx;
			 }
		  }
	   }
	  if ( n>0 ) out.end(); // end
	  curx += (float(chdesc->s) * scalingx) + float(charspc); // horizontal spacing
	}
   GS_TRACE1 ( "Curx:"<<curx<<" W:"<<(curx-p.x) );
 }

struct SnLines2FontOutput : public GsStrokeFont::Output
{	SnLines2* l;
	SnLines2FontOutput ( SnLines2* l2 ) { l=l2; }
	virtual void begin () { l->begin_polyline(); }
	virtual void end () { l->end_polyline(); }
	virtual void add ( float x, float y ) { l->push(x,y); };
};

void push_text ( SnLines2* l, const char* text, const GsPnt2& p, GsStrokeFont::Output& out, float charh, float wperc, float linespc, float charspc, bool wcoords, float* xmax )
{
	SnLines2FontOutput output(l);
	GsStrokeFont::draw ( text, p, output, charh, wperc, linespc, charspc, false, xmax );
}

void push_text_wc ( SnLines2* l, const char* text, const GsPnt2& p, GsStrokeFont::Output& out, float charh, float wperc, float linespc, float charspc, float* xmax )
{
	SnLines2FontOutput output(l);
	GsStrokeFont::draw ( text, p, output, charh, wperc, linespc, charspc, true, xmax );
}

